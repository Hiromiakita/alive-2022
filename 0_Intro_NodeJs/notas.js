// NPM -> Gestor paquetes (instalar y desintalar paquetes o librerías)
// Paquete (librería) -> Código que tiene funciones que nos ayudan a tarea específica (cálculos/fechas/peticiones)
// Comando para iniciar un proyecto que usará paquetes: npm init -> se creará automáticamente un package.json
// Package.json -> Contiene toda la infomarción para que nuestros proyectos funcionen
// Instalar un paquete: npm install nombre-del-paquete (hay que verificar que hagamos la instalación en la ubicación correcta)
// Al instalar un paquete se crean: node_modules y package-lock.json
// node_modules: todos los archivos que necesita nuestro paquete para funcionar correctamente
// package-lock.json: archivo que tiene todos los paquetes enlistados que se necesitan para nuestro aplicación
// Correr un archivo javaScript: node nombreDelArchivo.js
// para usar un paquete, tenemos que importarlo en el archivo en que lo usaremos

